package com.class8;

public class numOFrowsClms {
	public static void main(String[] args) {
		int [][]num= {
				
				{11,12,14,14},
				{21,22,23},
				{31,32,33,34},
			
		};
		for(int row=0;row<num.length;row++) {
			for(int col=0;col<num[row].length;col++) {
				System.out.print(num[0][1]+" ");
			}
		System.out.println();
		}
	}

}
